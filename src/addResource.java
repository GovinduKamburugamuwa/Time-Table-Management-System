    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        try {
            stmt = con.createStatement();

            int sClsId = Integer.parseInt(Claa_Num.getText());
            int sNumseats = Integer.parseInt(Claa_Num.getText());
            int snumWht = Integer.parseInt(numWht.getText());
            int snumPro = Integer.parseInt(numPro.getText());
            int snumMar = Integer.parseInt(numMar.getText());

            String query = "UPDATE allclassrooms SET ClassNumber = ?, Number_of_Seats = ?, White_Boards = ?, Projectors = ?, Markers = ? WHERE ClassNumber = ?";

            PreparedStatement preparedStmt = con.prepareStatement(query);

            preparedStmt.setInt(1, sClsId);
            preparedStmt.setInt(2, sNumseats);
            preparedStmt.setInt(3, snumWht);
            preparedStmt.setInt(4, snumPro);
            preparedStmt.setInt(5, snumMar);
            preparedStmt.setInt(6, sClsId);

            preparedStmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "UPDATED");

        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_updateActionPerformed

