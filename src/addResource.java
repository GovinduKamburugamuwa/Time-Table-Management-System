    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:   
        try {
            stmt = con.createStatement();

            int sClsId = Integer.parseInt(Claa_Num.getText());

            String query = "SELECT * FROM allclassrooms";

            rs = stmt.executeQuery(query);

            while (rs.next()) {
                if (sClsId == rs.getInt("ClassNumber")) {
                    numWht.setText(rs.getString("White_Boards"));
                    numPro.setText(rs.getString("Projectors"));
                    numMar.setText(rs.getString("Markers"));

                }
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_searchActionPerformed

